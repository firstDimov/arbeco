                        </blockquote>
                        <p>
                            Также фибробетон отличается солидной морозостойкостью и отличной водонепроницаемостью, что является важным для любого материала, используемого в строительстве зданий. Благодаря таким характеристикам бетон относится к уникальной группе современных конструктивных строительных материалов с ценными характеристиками. А получают такой бетон при введении в общую смесь Fibre, то есть армирующих уникальных волокон, добавляемых обычно в виде сетки или прорезанной фибры. Таким материалом для армирования бетона может быть металлическая фибра, стеклянная, синтетическая или же сделанная из природных материалов типа асбест или базальт. Именно по применению вида армирующей фибры современный бетон разделяется на классы и типы. Фибробетон имеет определенные именования, зависящее от типа армирующей фибры, то есть, если фибра сделана из стали, то получается сталефибробетон, а если из стекла, то стеклофибробетон. 
                        </p>
                        <p>
                            Фибра, то есть армирующие волокна добавляются в смесь тремя стандартными способами, это смешивание, общее напыление или же премиксинг, от такого способа добавления сильно зависят характеристики. Фибра может располагаться произвольно и хаотично или же в оперенном порядке, если волокна из стали, то им придается определенный порядок при помощи магнита, который выстраивает фибру. Если фибра сделана из искусственного материла, то для достижения определенного расположения волокон фибры выкладываются в виде специальной сетки, благодаря чему бетон становится более прочным и устойчивым. 
                        </p>
                        <p>
                            Технология получения фибробетона зависит от типа используемой фибры, сам бетон производится в специальных бетономешалках, а чтобы получить еще более качественный материал добавляют пластификаторы и ряд других добавок и заполнителей. Главная сфера использования. Фибробетон отличается огромной сферой использования, в частности, он является идеальным материалом для облицовки различных тоннелей и резервуаров. Часто используется в разных сферах гражданского современного строительства, для реконструкции сейфов и хранилищ, а также гидротехнических строений и зданий. Используется для построения мостов, плотин и дамб, а также каналов рек и шлюзов. Из такого бетона устраивают отделения атомных современных электростанций, а также контейнеры для хранения отходов радиоактивных элементов. Часто фибробетон используется для устройства различных покрытий дорог.
                        </p>
